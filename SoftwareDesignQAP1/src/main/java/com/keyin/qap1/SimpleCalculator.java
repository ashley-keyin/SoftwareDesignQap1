package com.keyin.qap1;

//A simple calculator program to take in 2 numbers and add, subtract or divide
//~ 45 minutes to complete
//used old lectures and google

public class SimpleCalculator {

    private int num1;
    private int num2;


    public int add (int num1, int num2){
        return num1 + num2;
    }

    public int sub (int num1, int num2){
        return num1 - num2;
    }

    public int div (int num1, int num2){
        return num1 / num2;
    }
}
